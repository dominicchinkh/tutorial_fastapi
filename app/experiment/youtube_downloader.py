import os
import tempfile
import yt_dlp

from fastapi import APIRouter, HTTPException, BackgroundTasks
from fastapi.responses import FileResponse

from ..models.video_request import VideoRequest

router = APIRouter(
    prefix="/youtube",
    tags=[
        "youtube"
    ],
    dependencies=[
        # Depends(get_token_header)
    ],
    responses={
        404: {
            "description": "Not found"
        }
    },
)

def remove_file(path: str):
    """Callback to delete the file from the server after it has been sent."""
    if os.path.exists(path):
        os.remove(path)

@router.post("/download")
async def download_youtube_video(request: VideoRequest, background_tasks: BackgroundTasks):
    video_url = str(request.url)
    
    # Create a unique temporary directory to avoid file collisions
    temp_dir = tempfile.mkdtemp()
    
    # Configure yt-dlp options
    # 'best' gets the best pre-merged format (usually up to 720p without needing FFmpeg setup)
    ydl_opts = {
        'format': 'best', 
        'outtmpl': os.path.join(temp_dir, '%(title)s.%(ext)s'),
        'quiet': True,
    }
    
    try:
        with yt_dlp.YoutubeDL(ydl_opts) as ydl:
            # Extract video metadata and trigger download
            info_dict = ydl.extract_info(video_url, download=True)
            
            # Retrieve the exact filename generated by yt-dlp
            filename = ydl.prepare_filename(info_dict)
            
        if not os.path.exists(filename):
            raise HTTPException(status_code=500, detail="Downloaded file could not be found.")

        # Clean up the file automatically after the response finishes transmitting
        background_tasks.add_task(remove_file, filename)
        background_tasks.add_task(os.rmdir, temp_dir)

        # Send the file back to the client
        return FileResponse(
            path=filename,
            media_type="video/mp4",
            filename=os.path.basename(filename)
        )

    except Exception as e:
        # Fallback cleanup if extraction/download fails halfway through
        if os.path.exists(temp_dir):
            import shutil
            shutil.rmtree(temp_dir)
        raise HTTPException(status_code=400, detail=f"Failed to process video: {str(e)}")

@router.post("/playlist")
async def list_youtube_video(request: VideoRequest):

    # Use the dedicated playlist URL format
    playlist_url = str(request.url)

    ydl_opts = {
        'quiet': True,
        'extract_flat': True,  # Extracts metadata without downloading the video
    }

    urls = {}
    
    with yt_dlp.YoutubeDL(ydl_opts) as ydl:
        result = ydl.extract_info(playlist_url, download=False)
        
        if 'entries' in result:
            print(f"Playlist Title: {result.get('title')}")
            print(f"Total Videos Found: {len(result['entries'])}\n")
            
            for entry in result['entries']:
                video_url = f"https://www.youtube.com/watch?v={entry['id']}"
                print(f"{entry.get('title')} -> {video_url}")
                
                urls[entry.get('title')] = video_url
                
    return urls
